package DoWhile;

public class BasicQuestion {
    public static void main(String[] args) {
        int sayi =  1;
        do {
            System.out.println(sayi + " "); // do bolumunde kod hemen calısır ve sayiyi yazdırıp bosluk koyar
            sayi++; // sonra sayiyi 1 artırır
        }while (sayi <1); // sonra da buraya bakar burasi saglamadıgı icin calısmaz
    }
}
