package Inheritence_ConstructorCall;

public class VariableYazdırmaSıralaması {
    /*
    1- icerisinde oldugumuz scope
    2- icerisinde oldugumuz class level’daki
instance variable’lar
    3- parent class’daki instance variable’lar
****************************************************
    Variable’i constructor icerisinde kullanacaksak ve bu siralamaya uymak istemezsek, kullanmak istedigimiz variable’i kendimiz belirleyebiliriz.
1- sadece variable ismini yazarsak icerisinde oldugumuz scope’daki variable’in degerini
2- this.variableIsmi’ni yazarsak icerisinde oldugumuz class’daki instance variable’in degerini
3- super.variableIsmi’ni yazarsak parent class’daki instance variable’in degerini bize döndürür.
NOT: super( ) ve this ( ) constructor call icerisinde oldugu constructor’in
 ilk satirinda olabilir ve sadece biri kullanilabilir. Ancak this. ve super. , istenen satirda ve istenen sayida kullanilabilir.
     */
}
